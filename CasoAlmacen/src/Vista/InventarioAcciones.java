    private void centrarTextoEnJTextPane(JTextPane pane, String texto) {
    // Centrado horizontal
    StyledDocument doc = pane.getStyledDocument();
    SimpleAttributeSet center = new SimpleAttributeSet();
    StyleConstants.setAlignment(center, StyleConstants.ALIGN_CENTER);
    doc.setParagraphAttributes(0, doc.getLength(), center, false);

    // Centrado vertical (simulado)
    int lineasVisibles = 4; // Ajusta segÃºn el tamaÃ±o del text pane
    int lineasAntes = lineasVisibles / 2;

    StringBuilder sb = new StringBuilder();
    for (int i = 0; i < lineasAntes; i++) {
        sb.append("\n");
    }
    sb.append(texto);

    pane.setText(sb.toString());
}

